"""접근 레벨(권한 태그)을 순서형으로 모델링한다 (ADR-005·012·017).

`권한 태그`는 접근 통제의 하드 필터다. 위계를 정수로 모델링해
"사용자 레벨 >= 문서 레벨"이면 접근 가능하도록 비교한다.
숫자가 클수록 더 넓은 접근 권한을 가진다 — 마스터는 멤버 문서를 보지만
멤버는 마스터 문서를 볼 수 없다.

역할(ADR-017): `마스터`(2)=관리자(API키·외부소스·멤버 관리 + 최상위 문서 접근),
`멤버`(1)=일반 사용자. `임직원`/`사장`은 하위호환 **별칭**이다(같은 정수값 →
기존 코드·테스트가 그대로 동작, `.name`만 멤버/마스터로 정규화).
"""

from enum import IntEnum


class AccessLevel(IntEnum):
    """문서·사용자의 접근 레벨. 값이 클수록 넓은 권한 (IntEnum이라 순서 비교 가능)."""

    멤버 = 1
    마스터 = 2
    # 하위호환 별칭 — 같은 정수값이라 IntEnum이 정규(멤버/마스터)의 별칭으로 둔다.
    임직원 = 1
    사장 = 2

    def can_access(self, doc_level: "AccessLevel") -> bool:
        """사용자 레벨(self)이 문서 레벨 이상이면 True (권한 인지 하드 필터)."""
        return self >= doc_level

    @property
    def is_master(self) -> bool:
        """관리자(마스터) 여부 — API키·외부소스·멤버 관리 권한 판정."""
        return self == AccessLevel.마스터
